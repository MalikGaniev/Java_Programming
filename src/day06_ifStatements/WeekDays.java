package day06_ifStatements;

public class WeekDays {
    public static void main(String[] args) {
        int n = 1;
       String result = "";
        if(n==1){
            result = "Monday";
        }
        if(n==2){
            result = "Tuesda";
        }
        if(n==3){
            result = "Wednesday";
        }
        if(n==4){
            result = "Thursday";
        }
        if(n==5){
            result = "Friday";
        }
        if(n==6){
            result = "Satuday";
        }
        if(n==7){
            result = "Sunday";
        }
        System.out.println(result);




    }




}
